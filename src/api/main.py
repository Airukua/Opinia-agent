class OrchestrateRequest(BaseModel):
    """Request payload for running the full comment analytics pipeline."""

    video_input: Optional[str] = Field(None, description="YouTube URL or ID")
    video_inputs: Optional[List[str]] = Field(None, description="List of YouTube URLs/IDs")
    output_dir: str = Field("outputs", description="Output directory")
    output_json_name: Optional[str] = Field(None, description="Optional output JSON file name")
    anonymize_authors: bool = Field(True, description="Mask author names")
    topic_with_llm: bool = Field(True, description="Enable LLM topic labeling")
    save_split_outputs_flag: bool = Field(True, description="Write evidence/insights separately")


class OrchestrateMeta(BaseModel):
    """Metadata for the orchestrator response."""

    request_id: str
    processed_at: str
    duration_ms: int
    items: int


class OrchestrateItem(BaseModel):
    """File references generated by the pipeline."""

    video_id: str
    output_path: str
    evidence_path: Optional[str]
    llm_insights_path: Optional[str]


class OrchestrateResponse(BaseModel):
    """Response payload containing full pipeline outputs."""

    meta: OrchestrateMeta
    results: List[OrchestrateItem]


def _collect_inputs(payload: OrchestrateRequest) -> List[str]:
    """Collects and sanitizes video inputs from request payload.

    Args:
        payload: Incoming request payload.

    Returns:
        List of normalized YouTube URLs/IDs to process.

    Example usage:
        >>> req = OrchestrateRequest(video_input="dQw4w9WgXcQ")
        >>> _collect_inputs(req)
        ['dQw4w9WgXcQ']
    """
    inputs: List[str] = []
    if payload.video_input:
        inputs.append(payload.video_input.strip())
    if payload.video_inputs:
        inputs.extend(item.strip() for item in payload.video_inputs if item and item.strip())
    return [item for item in inputs if item]


def _build_output_name(base_name: Optional[str], idx: int, total: int) -> Optional[str]:
    """Builds a unique output filename for multi-item requests.

    Args:
        base_name: Requested output name, if any.
        idx: Zero-based index of the current item.
        total: Total number of items in the request.

    Returns:
        Output filename with suffix when multiple items are processed.
    """
    if not base_name or total <= 1:
        return base_name
    path = Path(base_name)
    suffix = path.suffix or ".json"
    stem = path.stem
    return f"{stem}_{idx + 1}{suffix}"


def _env_bool(name: str, default: bool) -> bool:
    """Reads a boolean value from environment variables."""
    value = os.getenv(name)
    if value is None:
        return default
    return value.strip().lower() in {"1", "true", "yes", "on"}


def _env_int(name: str, default: Optional[int]) -> Optional[int]:
    """Reads an integer value from environment variables."""
    value = os.getenv(name)
    if value is None or value == "":
        return default
    try:
        return int(value)
    except ValueError:
        return default


def _resolve_output_dir(output_dir: str) -> str:
    """Resolves output directory within the allowed base directory.

    Args:
        output_dir: Requested relative output directory.

    Returns:
        Absolute, sanitized output directory.
    """
    base = Path(OUTPUT_BASE_DIR).resolve()
    target = (base / output_dir).resolve() if output_dir else base
    if base not in target.parents and target != base:
        raise HTTPException(status_code=400, detail="invalid_output_dir")
    return str(target)


def _sanitize_output_name(name: Optional[str]) -> Optional[str]:
    """Validates output filename to prevent path traversal."""
    if not name:
        return None
    candidate = Path(name)
    if candidate.name != name or ".." in candidate.parts:
        raise HTTPException(status_code=400, detail="invalid_output_json_name")
    return name


def _build_result_paths(
    *,
    output_dir: str,
    output_name: str,
    video_id: str,
    save_split_outputs_flag: bool,
) -> OrchestrateItem:
    """Builds file paths returned to the caller."""
    base = Path(output_dir)
    output_path = str(base / output_name)
    evidence_path = str(base / f"evidence_{video_id}.json") if save_split_outputs_flag else None
    llm_path = (
        str(base / f"llm_insights_{video_id}.json") if save_split_outputs_flag else None
    )
    return OrchestrateItem(
        video_id=video_id,
        output_path=output_path,
        evidence_path=evidence_path,
        llm_insights_path=llm_path,
    )


@app.middleware("http")
async def request_context_middleware(request: Request, call_next):
    """Attaches request id and timing metadata to each response."""
    content_length = request.headers.get("content-length")
    if content_length and int(content_length) > MAX_REQUEST_BYTES:
        raise HTTPException(status_code=413, detail="request_too_large")
    request_id = request.headers.get("X-Request-ID") or str(uuid.uuid4())
    request.state.request_id = request_id
    start_time = time.perf_counter()
    response = await call_next(request)
    duration_ms = int((time.perf_counter() - start_time) * 1000)
    response.headers["X-Request-ID"] = request_id
    response.headers["X-Process-Time-MS"] = str(duration_ms)
    return response


