@app.get("/results/{filename}")
def get_result_file(filename: str) -> FileResponse:
    """Downloads a generated JSON result file by filename."""
    if not filename or "/" in filename or "\\" in filename:
        raise HTTPException(status_code=400, detail="invalid_filename")
    base = Path(OUTPUT_BASE_DIR).resolve()
    target = (base / filename).resolve()
    if base not in target.parents:
        raise HTTPException(status_code=400, detail="invalid_filename")
    if not target.exists():
        raise HTTPException(status_code=404, detail="file_not_found")
    return FileResponse(str(target), media_type="application/json")


@app.post("/analyze", response_model=OrchestrateResponse)
def orchestrate(payload: OrchestrateRequest, request: Request) -> OrchestrateResponse:
    """Runs the full pipeline for one or more YouTube inputs.

    Args:
        payload: Request body containing inputs and options.
        request: FastAPI request context.

    Returns:
        OrchestrateResponse with metadata and full results.
    """
    inputs = _collect_inputs(payload)
    if not inputs:
        raise HTTPException(status_code=400, detail="video_input or video_inputs is required")

    start_time = time.perf_counter()
    results: List[OrchestrateItem] = []
    total = len(inputs)

    safe_output_dir = _resolve_output_dir(payload.output_dir)
    logger.info("Orchestrate started: items=%s request_id=%s", total, request.state.request_id)
    for idx, video_input in enumerate(inputs):
        base_name = _sanitize_output_name(payload.output_json_name)
        output_name = _build_output_name(base_name, idx, total)
        video_id = extract_video_id(video_input)
        try:
            run_orchestrator(
                video_input=video_input,
                api_key=None,
                output_dir=safe_output_dir,
                output_json_name=output_name,
                anonymize_authors=payload.anonymize_authors,
                topic_with_llm=payload.topic_with_llm,
                llm_model=os.getenv("OLLAMA_MODEL"),
                llm_base_url=os.getenv("OLLAMA_BASE_URL"),
                llm_enabled=_env_bool("LLM_ENABLED", True),
                llm_batch_size=_env_int("LLM_BATCH_SIZE", None),
                llm_comment_limit=_env_int("LLM_COMMENT_LIMIT", None),
                llm_include_history=(
                    None
                    if os.getenv("LLM_INCLUDE_HISTORY") is None
                    else _env_bool("LLM_INCLUDE_HISTORY", True)
                ),
                llm_timeout_seconds=_env_int("OLLAMA_TIMEOUT", None),
                llm_provider=os.getenv("LLM_PROVIDER"),
                llm_api_key=os.getenv("LLM_API_KEY"),
                llm_api_base_url=os.getenv("LLM_API_BASE_URL"),
                llm_simple_mode=(
                    None
                    if os.getenv("LLM_SIMPLE_MODE") is None
                    else _env_bool("LLM_SIMPLE_MODE", False)
                ),
                save_split_outputs_flag=payload.save_split_outputs_flag,
            )
        except ValueError as exc:
            logger.warning("Bad request: %s", exc)
            raise HTTPException(status_code=400, detail=str(exc)) from exc
        except Exception as exc:  # pragma: no cover - defensive guard
            logger.exception("Orchestrator failed")
            raise HTTPException(status_code=500, detail="internal_server_error") from exc

        results.append(
            _build_result_paths(
                output_dir=safe_output_dir,
                output_name=output_name or f"orchestrator_{video_id}.json",
                video_id=video_id,
                save_split_outputs_flag=payload.save_split_outputs_flag,
            )
        )

    duration_ms = int((time.perf_counter() - start_time) * 1000)
    meta = OrchestrateMeta(
        request_id=request.state.request_id,
        processed_at=datetime.now(timezone.utc).isoformat(),
        duration_ms=duration_ms,
        items=len(results),
    )
    return OrchestrateResponse(meta=meta, results=results)
